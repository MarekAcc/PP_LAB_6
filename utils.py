"""Simple calculator"""
def add(a: int, b: int) -> int:
    """Add function"""
    return a + b


def subtract(a: int, b: int) -> int:
    """Substract function"""
    return a - b


def multiply(a: int, b: int) -> int:
    """Multiply function"""
    return a * b


def divide(a: int, b: int) -> float:
    """Divide function"""
    return a / b
